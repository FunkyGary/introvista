// 定義類型
export type CategoryID =
  (typeof categories)[keyof typeof categories][number]['value']
export type MainCategory = keyof typeof categories
export type SubCategory = (typeof categories)[MainCategory][number]

// 輔助函數
export const getMainCategories = () => Object.keys(categories) as MainCategory[]

export const getSubCategories = (mainCategory: MainCategory) =>
  categories[mainCategory]

export const getCategoryLabel = (categoryId: CategoryID): string => {
  for (const mainCategory of Object.values(categories)) {
    const found = mainCategory.find((sub) => sub.value === categoryId)
    if (found) return found.label
  }
  return ''
}
